  Future<Map<String, dynamic>> translateMap({
    required Map<String, dynamic> inputMap,
    String? from,
    required String to,
   Function(double progress) ? progressCallback,
  }) async {
    Map<String, dynamic> translated = {};
    final int total = inputMap.length;
    int counter = 0;

    for (var source in inputMap.keys) {
      final String valueOfKey = inputMap[source] ?? "";
      final translatedValue =
          await translateWord(valueOfKey, from: from, to: to);
      translated[source] = translatedValue.text;
      progressCallback?.call(counter / total);
      counter ++;

      printDM(translated.toString());
    }
    printDM(translated.toString());

    return Future.value(translated);
  }

  Future<Translation> translateWord(
    String source, {
    String? from,
    required String to,
  }) {
    final translator = GoogleTranslator();
    final input = source;
    final translation =
        translator.translate(input, from: from = 'auto', to: to);
    return translation;
  }

